def insertion_sort(s):
   """
   Input: list s to be sorted
   Output: sorted list
   """

   arr = range(len(s));

   del arr[0];

   for i in arr:

      j = i
      
      data = s[j]

      while (j > 0 and s[j] < s[j-1]):

         s[j] = s[j - 1]
   
         s[j-1] = data
      
         j = j - 1;   

   return s

