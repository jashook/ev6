#ifndef __FORWARD_NODE_HPP__
#define __FORWARD_NODE_HPP__

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

namespace ev6 {
namespace el {

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

template < typename __Key, typename __Value = __Key > class forward_node
{
   public:   // member variables

      forward_node* _m_next;
      __Key _m_key;
      __Value _m_data;

   public:   // contructor | destructor
  
      forward_node() : _m_data(0), _m_key(0), _m_next(0) { }
      forward_node(__Value _Data, forward_node* _Next) : _m_data(_Data), _m_next(_Next), _m_key(0) { }

};   // end of class forward_node

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

}   // end of namespace(el)
}   // end of namespace(ev6)

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

#endif   // __FORWARD_NODE_HPP__
