#ifndef PAIR
#define PAIR

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

namespace ev4 {

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

template <typename __Type, typename __Second_Type> class pair
{
   public:   //   member variables
      __Type* _m_data;
      __Second_Type _m_second_data;

   public:   //   member functions

      __Type& get_data() { return _m_data; }
      __Second_Type& get_second_data() { return _m_second_data; }

   public:   //   constructor | destructor

      pair() : _m_data(0), _m_second_data(0) { }
      pair(__Type& _data, __Second_Type _second_data) : _m_data(_data), _m_second_data(_second_data) { }
};

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

}   //   end of namespace(ev4)

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

#endif   /*   PAIR   */
