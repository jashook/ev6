#ifndef __STRING_S__
#define __STRING_S__

/* ************************************************************************** */
/* ************************************************************************** */
/* Function: str_cpy                                                          */
/*                                                                            */
/* Parameters:                                                                */
/*                                                                            */
/*    r0: pointer to a string                                                 */
/*    r1: pointer to a a copy location
/*                                                                            */
/* Returns:                                                                   */
/*                                                                            */
/* ************************************************************************** */
/* ************************************************************************** */

str_cpy:

         ldrb  r3, [r0], #1      @ r3 = *(r3++)

         strb  r1, r3            @ *r1 = r3

         cmp   [r0], #0          @ *r0 = \0?

         bne   str_cpy

         mov   pc, lr

/* ************************************************************************** */
/* ************************************************************************** */
/* Function: str_len                                                          */
/*                                                                            */
/* Parameters:                                                                */
/*                                                                            */
/*    r0: pointer to a string                                                 */
/*                                                                            */
/* Returns:                                                                   */
/*                                                                            */
/*    r1: size of the string                                                  */
/*                                                                            */
/* ************************************************************************** */
/* ************************************************************************** */

str_len:

         mov   r1, #0            @ r1 = 0

str_len_loop:

         ldrb  r2, [r0], #1      @ r2 = *(r0++)
         add   r1, r1, #1
         cmp   r2, #0            @ null terminator?

         bne   str_len_loop      @ jump if not equal back to str_len_loop

         sub   r1, r1, #1

         mov   pc, lr

/* ************************************************************************** */
/* ************************************************************************** */

#endif /* __STRING_S__ */

