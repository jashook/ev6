use strict;
use warnings;

################################################################################
################################################################################

package Line;

################################################################################
################################################################################
# Constructor
################################################################################
################################################################################

sub new {
 
   my (@_Arr) = $_;
  
   my $_Self = {

      m_line => $_Arr[0],
      m_line_number => $_Arr[1]

   };

   bless $_Self, 'Line';
   return $_Self;

}

################################################################################
################################################################################
# Methods
################################################################################
################################################################################

sub line {
   
   my ($_Self, $_Line) = @_; # create a local self and line variable

   $_Self->{ m_line } = $_Line if defined($_Line);

   return $_Self->{ m_line };

}

sub line_number {

   my ($_Self, $_LineNumber) = @_;

   $_Self->{ m_line_number } = $_LineNumber if defined($_LineNumber);

   return $_Self->{ m_line_number };

}

################################################################################
################################################################################
# end of class
################################################################################
################################################################################

1
