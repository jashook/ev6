use strict;
use warnings;

use Path::Class;

################################################################################
################################################################################


################################################################################
################################################################################

my $dir = dir("."); # current directory

while (my $file = $dir->next)
{

   next if $file->is_dir();

   print substr $file->stringify, 2;

   print "\n";

}

