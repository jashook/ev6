#ifndef __BIDIRECTIONAL_NODE_HPP__
#define __BIDIRECTIONAL_NODE_HPP__

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

namespace ev6 {
namespace el {

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

template <typename __Type> class bidirectional_node
{
   public:   // member variables
   
      bidirectional_node* m_next;
      bidirectional_node* m_prev;
      __Type m_data;

   public:   // constructor | destructor

      bidirectional_node() : m_data(0), m_next(0), m_prev(0) { }
      bidirectional_node(__Type _Data, bidirectional_node* _Next, bidirectional_node* _Prev) : m_data(_Data), m_next(_Next), m_prev(_Prev) { }

   public:   // member functions
      
      void realloc(__Type& _Data, bidirectional_node* _Next, bidirectional_node* _Prev)
      {
         m_next = _Next;
         m_prev = _Prev;
         m_data = _Data;
      }

};

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

}   // end of namespace(el)
}   // end of namespace(ev6)

///////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////

#endif   // __BIDIRECTIONAL_NODE_HPP__
