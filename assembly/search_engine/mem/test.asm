; 
; Procedure: _main
;
; Arguements:
;
;     VOID
;
;  Returns:
;
;     VOID
;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;
;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;;

_start:                             ; entry point

         nop

         mov   [memory_loc], dword 10

         mov   [memory_loc + 4], dword 11

         mov   [memory_loc + 8], dword 12

         xor   eax, eax 

         mov   eax, 1               ; (sys_exit) assembly instruction

         mov   ebx, 0               ; return 0

         int   080h
